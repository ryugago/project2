using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class lookShadow : MonoBehaviour
{
    public TMPro.TMP_Text ChatText; // 실제 채팅이 나오는 텍스트
    public float delayTime;
    public float autoProceedDelay = 3f;

    public List<KeyCode> skipButton; // 대화를 빠르게 넘길 수 있는 키

    public string writerText = "";
    [Header("카메라 움직임")]
    public GameObject shadow; // 활성화할 오브젝트를 위한 변수
    private Transform cameraTransform; // 카메라의 Transform 컴포넌트
    bool isButtonClicked = false;

    public TMPro.TMP_Text quest;

    private bool trigger1;
    private bool trigger2 = true;

    public Light shadowlight;

    public AudioClip sfxclip;
    public AudioClip bgm;

    void Start()
    {
        cameraTransform = GameObject.FindGameObjectWithTag("MainCamera").transform;
        shadowlight.gameObject.SetActive(false);
        shadow.SetActive(false);
    }

    private void Update()
    {
        foreach (var element in skipButton) // 버튼 검사
        {
            if (Input.GetKeyDown(element))
            {
                isButtonClicked = true;
            }
        }
        if (trigger1&&trigger2)
        {
            trigger2 = false;
            shadowlight.gameObject.SetActive(true);
            StartCoroutine(TextPractice());
        }

    }

    // Start is called before the first frame update
    IEnumerator NormalChat(string narration)
    {
        int a = 0;
        writerText = "";

        //텍스트 타이핑 효과
        for (a = 0; a < narration.Length; a++)
        {
            writerText += narration[a];
            ChatText.text = writerText;
            yield return new WaitForSeconds(delayTime); // delayTime만큼 대기
            if (isButtonClicked)
            {
                ChatText.text = narration;
                a = narration.Length; // 버튼 눌리면 그냥 다 출력하게 함
                isButtonClicked = false;
            }
        }
        //yield return new WaitForSeconds(autoProceedDelay);


        float timer = 0f;
        while (timer < autoProceedDelay)
        {

            if (isButtonClicked)
            {
                isButtonClicked = false;
                break;
            }
            timer += Time.deltaTime;
            yield return null;
        }
    }

    IEnumerator FollowObjectRotation()
    {
        while (true)
        {
            if (shadow.activeSelf)
            {
                // 카메라를 오브젝트를 바라보게 회전
                Vector3 directionToLook = shadow.transform.position - cameraTransform.position;
                Quaternion rotationToLookAt = Quaternion.LookRotation(directionToLook);
                cameraTransform.rotation = Quaternion.Slerp(cameraTransform.rotation, rotationToLookAt, Time.deltaTime * 5f);

                yield return null;
            }
            else
            {

                break; // 코루틴 종료
            }
        }
    }

    IEnumerator removeobj()
    {
        yield return new WaitForSeconds(5f); // 1초 대기
        shadow.SetActive(false); // 오브젝트 비활성화
        //GameManager.canPlayerMove2 = true;
        yield return null;
    }

    IEnumerator TextPractice()
    {
        GameManager.canPlayerMove2 = false;
        shadow.SetActive(true);
        StartCoroutine(FollowObjectRotation());
        StartCoroutine(removeobj());
        SoundManager.instance.SFXPlay("shadow1", sfxclip);
        autoProceedDelay = 3f;
        yield return StartCoroutine(NormalChat("거기 누구 있어요?"));
        shadowlight.gameObject.SetActive(false);
        autoProceedDelay = 3f;
        yield return new WaitForSeconds(1f);
        shadowlight.gameObject.SetActive(true);
        yield return StartCoroutine(NormalChat("그림자가 사라졌어"));
        autoProceedDelay = 0f;
        shadowlight.gameObject.SetActive(false);
        SoundManager.instance.BgSoundPlay(bgm);
        GameManager.canPlayerMove2 = true;
        yield return StartCoroutine(NormalChat(" "));
    }

    private void OnTriggerEnter(Collider other)
    {
        if (other.tag == "Player")
            trigger1 = true;
    }
}
